#include<stdio.h>
int main11 ()
{
	/*
	运算表达式的基本概念：
	1：运算符
	2：操作数
	3：表达式
	4：结合性 
	5：优先级
	*/
	/*
	牢记：只要是表达式 就有一个计算结果
	*/

	8;//常量语句
	3 + 5;//加法语句  3  5 就是常量表达式
	int a = 3 + 5;//赋值语句  3+5 是其中的表达式

	/*
	算术运算符： + - * / % ( ) 
	相反数运算符：-
	*/
	a = 5 + 6 * 7 / 8 % (3 - 10);
	//算术运算特别的地方：
	//printf("%d %d\n",8%0,8/0);
	//负数参与的求余运算
	printf("%d\n",-9%2);// -9/2 ==-4  ...-1
	printf("%d\n", -9 % -2);//-9/-2 4 ...-1
	//相反数运算符：-
	int zi = 1;
	zi = -zi;
	zi = -zi;
	zi = -zi;
	printf("%d\n", - -zi);
	return 0;
}